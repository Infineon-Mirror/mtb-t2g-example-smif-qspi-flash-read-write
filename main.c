#include "cy_serial_flash_qspi.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
/* Memory Read/Write size */
#define PACKET_SIZE             (64u)

/* Used when an array of data is printed on the console */
#define NUM_BYTES_PER_LINE      (16u)

/* LED blink delay */
#define LED_TOGGLE_DELAY_MSEC   (1000u)

/* QSPI clock frequency */
#define QSPI_BUS_FREQUENCY_HZ   (50000000lu)

/* Flash data after erase */
#define FLASH_DATA_AFTER_ERASE  (0xFFu)

/* Offset address from the top of QSPI device region */
#define ADDRESS                 (smifMemConfigs[0]->deviceCfg->eraseSize)

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/

/*********************************************************************************************************************/
/*--------------------------------------------Private Variables/Constants--------------------------------------------*/
/*********************************************************************************************************************/

